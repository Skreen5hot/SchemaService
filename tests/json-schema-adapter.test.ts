// --- Full Round-Trip ---

test("full round-trip: infer from sample data → project to JSON Schema", () => {
  // Simulate a CISM from: [{ name: "Alice", age: 30 }, { name: "Bob", age: 25 }]
  const nameNode: SchemaNode = {
    id: "#/[]/name", kind: "primitive", primitiveType: "string",
    occurrences: 2, typeDistribution: { string: 2 },
  };
  const ageNode: SchemaNode = {
    id: "#/[]/age", kind: "primitive", primitiveType: "integer",
    occurrences: 2, typeDistribution: { integer: 2 },
  };
  const itemNode: SchemaNode = {
    id: "#/[]", kind: "object", occurrences: 2,
    properties: [
      { name: "name", target: nameNode, required: true, occurrences: 2, totalPopulation: 2 },
      { name: "age", target: ageNode, required: true, occurrences: 2, totalPopulation: 2 },
    ],
  };
  const root: SchemaNode = { id: "#", kind: "array", itemType: itemNode, occurrences: 1 };
  const cism = buildCISM(root, "deadbeef");

  const schema = toJsonSchema(cism);

  strictEqual(schema.$schema, "https://json-schema.org/draft/2020-12/schema");
  strictEqual(schema.type, "array");
  const items = schema.items as Record<string, unknown>;
  strictEqual(items.type, "object");
  deepStrictEqual(items.required, ["name", "age"]);
  const props = items.properties as Record<string, Record<string, unknown>>;
  deepStrictEqual(props.name, { type: "string" });
  deepStrictEqual(props.age, { type: "integer" });
});

