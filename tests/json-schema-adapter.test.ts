// --- Union Mapping ---

test("union → { oneOf: [...members] }", () => {
  const m0: SchemaNode = {
    id: "#/|0", kind: "primitive", primitiveType: "integer", occurrences: 2,
  };
  const m1: SchemaNode = {
    id: "#/|1", kind: "object", occurrences: 1, properties: [],
  };
  const root: SchemaNode = {
    id: "#", kind: "union", members: [m0, m1], nullable: false, occurrences: 3,
  };
  const schema = toJsonSchema(buildCISM(root));
  const oneOf = schema.oneOf as Record<string, unknown>[];
  strictEqual(oneOf.length, 2);
  deepStrictEqual(oneOf[0], { type: "integer" });
  deepStrictEqual(oneOf[1], { type: "object", properties: {} });
});

test("nullable union → oneOf includes { type: 'null' } member", () => {
  const m0: SchemaNode = {
    id: "#/|0", kind: "primitive", primitiveType: "string", occurrences: 3,
  };
  const m1: SchemaNode = {
    id: "#/|1", kind: "object", occurrences: 2, properties: [],
  };
  const root: SchemaNode = {
    id: "#", kind: "union", members: [m0, m1], nullable: true, occurrences: 6,
  };
  const schema = toJsonSchema(buildCISM(root));
  const oneOf = schema.oneOf as Record<string, unknown>[];
  strictEqual(oneOf.length, 3);
  deepStrictEqual(oneOf[2], { type: "null" });
});

// --- Nested Structures ---

test("nested object → recursive projection", () => {
  const city: SchemaNode = {
    id: "#/address/city", kind: "primitive", primitiveType: "string", occurrences: 1,
  };
  const address: SchemaNode = {
    id: "#/address", kind: "object", occurrences: 1,
    properties: [edge("city", city)],
  };
  const root: SchemaNode = {
    id: "#", kind: "object", occurrences: 1,
    properties: [edge("address", address)],
  };
  const schema = toJsonSchema(buildCISM(root));
  const addressSchema = (schema.properties as Record<string, Record<string, unknown>>).address;
  strictEqual(addressSchema.type, "object");
  const citySchema = (addressSchema.properties as Record<string, Record<string, unknown>>).city;
  deepStrictEqual(citySchema, { type: "string" });
});

test("array of objects → array with object items", () => {
  const nameNode: SchemaNode = {
    id: "#/[]/name", kind: "primitive", primitiveType: "string", occurrences: 3,
  };
  const itemNode: SchemaNode = {
    id: "#/[]", kind: "object", occurrences: 3,
    properties: [edge("name", nameNode)],
  };
  const root: SchemaNode = { id: "#", kind: "array", itemType: itemNode, occurrences: 1 };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual(schema.type, "array");
  const items = schema.items as Record<string, unknown>;
  strictEqual(items.type, "object");
  const nameSchema = (items.properties as Record<string, Record<string, unknown>>).name;
  deepStrictEqual(nameSchema, { type: "string" });
});

