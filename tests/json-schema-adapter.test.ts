// --- Object Mapping ---

test("object → { type: 'object', properties: {...} }", () => {
  const nameNode: SchemaNode = {
    id: "#/name", kind: "primitive", primitiveType: "string", occurrences: 1,
  };
  const root: SchemaNode = {
    id: "#", kind: "object", occurrences: 1,
    properties: [edge("name", nameNode)],
  };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual(schema.type, "object");
  const props = schema.properties as Record<string, Record<string, unknown>>;
  deepStrictEqual(props.name, { type: "string" });
});

test("required properties listed in required array", () => {
  const a: SchemaNode = { id: "#/a", kind: "primitive", primitiveType: "string", occurrences: 1 };
  const b: SchemaNode = { id: "#/b", kind: "primitive", primitiveType: "integer", occurrences: 1 };
  const root: SchemaNode = {
    id: "#", kind: "object", occurrences: 1,
    properties: [edge("a", a, true), edge("b", b, false)],
  };
  const schema = toJsonSchema(buildCISM(root));
  deepStrictEqual(schema.required, ["a"]);
});

test("required is omitted when no required properties", () => {
  const a: SchemaNode = { id: "#/a", kind: "primitive", primitiveType: "string", occurrences: 1 };
  const root: SchemaNode = {
    id: "#", kind: "object", occurrences: 1,
    properties: [edge("a", a, false)],
  };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual("required" in schema, false);
});

test("empty object → { type: 'object', properties: {} }", () => {
  const root: SchemaNode = { id: "#", kind: "object", occurrences: 1, properties: [] };
  const schema = toJsonSchema(buildCISM(root));
  deepStrictEqual(schema.properties, {});
});

