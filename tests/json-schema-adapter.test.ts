import { deepStrictEqual, strictEqual, ok } from "node:assert";
import type { CISMRoot, SchemaNode, SchemaEdge } from "../src/kernel/types.js";
import { createDefaultConfig } from "../src/kernel/types.js";
import { toJsonSchema } from "../src/kernel/json-schema-adapter.js";

console.log("json-schema-adapter tests\n");

let passed = 0;
let failed = 0;

function test(name: string, fn: () => void): void {
  try {
    fn();
    console.log(`  \u2713 PASS: ${name}`);
    passed++;
  } catch (error) {
    console.error(`  \u2717 FAIL: ${name}`);
    console.error("   ", error instanceof Error ? error.message : String(error));
    failed++;
  }
}

// --- Helper ---

function buildCISM(root: SchemaNode, inputHash?: string): CISMRoot {
  return {
    version: "1.3",
    generatedAt: "2026-03-01T00:00:00.000Z",
    config: createDefaultConfig(),
    root,
    nodeIndex: new Map([[root.id, root]]),
    inputHash,
  };
}

function edge(name: string, target: SchemaNode, required = true): SchemaEdge {
  return { name, target, required, occurrences: 1, totalPopulation: 1 };
}

// --- Schema Metadata ---

test("includes $schema for Draft 2020-12", () => {
  const root: SchemaNode = { id: "#", kind: "object", occurrences: 1, properties: [] };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual(schema.$schema, "https://json-schema.org/draft/2020-12/schema");
});

test("includes title and $comment", () => {
  const root: SchemaNode = { id: "#", kind: "object", occurrences: 1, properties: [] };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual(schema.title, "Inferred Schema");
  strictEqual(schema.$comment, "Generated by BIBSS v1.3");
});

test("$id includes inputHash when present", () => {
  const root: SchemaNode = { id: "#", kind: "object", occurrences: 1, properties: [] };
  const schema = toJsonSchema(buildCISM(root, "abcdef1234567890"));
  strictEqual(schema.$id, "urn:bibss:inferred:abcdef1234567890");
});

test("$id is omitted when inputHash is not set", () => {
  const root: SchemaNode = { id: "#", kind: "object", occurrences: 1, properties: [] };
  const schema = toJsonSchema(buildCISM(root));
  strictEqual("$id" in schema, false);
});

